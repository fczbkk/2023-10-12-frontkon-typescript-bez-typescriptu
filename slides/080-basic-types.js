/**
 * @param {string} message
 */
function log (message) {
  console.log(message)
}

/**
 * @param {number} a
 * @param {number} b
 * @returns {number}
 */
function subtract (a, b) {
  return a - b
}

/**
 * @param {number | string} a
 * @param {number | string} b
 * @returns {number}
 */
function add (a, b) {
  return Number(a) + Number(b)
}
