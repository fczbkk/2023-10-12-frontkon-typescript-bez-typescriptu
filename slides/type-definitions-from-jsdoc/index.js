/**
 * Adds two numbers or strings.
 * @param {string | number} a
 * @param {string | number} b
 * returns {string | number}
 */
export function add(a, b) {
    return a + b;
}

/**
 * Subtracts two numbers.
 * @param {number} a
 * @param {number} b
 * returns {number}
 */
export function subtract(a, b) {
    return a - b;
}
